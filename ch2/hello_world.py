# Print a simple message

print("Hello world!")

# Store a message in a variable, then print it

message = "Hello, cruel world!"
print(message)

# Change the content of a variable, then print it

message = "Hello, world of milk and honey!"
print (message)

# Remember
# 1. Variable names can contain only letters, numbers & underscores.
# They can start with a letter or underscore, but not with a number.
# 2. Spaces are not allowed in variable names.
# 3. Avoid using python keywords/functions as variable names.
# 4. Variables should be short and descriptive.



