cars = ['bmw', 'audi', 'toyota', 'subaru']
print("Here is the original list:")
print(cars)

print("\nHere is ths sorted list:")
print(sorted(cars))

print("\nHere is the original list again:")
print(cars)

cars.reverse()

print("\nHere is a permanently reversed list:")
print(cars)

print("\nLet's find the length of this list:")
print(len(cars))
